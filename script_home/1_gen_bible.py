def make_file(testament, category, book, slug, part, chapters):
    title = f"《{testament}》{book}《{part}》：{chapters} 章"
    if book == "詩篇":
        title = f"《{testament}》{book}《{part}》：{chapters} 篇"
    filename = BASE / f"{slug}-{part:02d}.md"
    content = f"""---
title: "{title}"
date: {DATE}
draft: false
description: ""
testament: "{testament}"
category: "{category}"
book: "{book}"
part: {part}
chapters: "{chapters}"
showToc: true
ShowBreadCrumbs: true
---

（內容待新增）
"""
    filename.write_text(content, encoding="utf-8")
    return filename.name


